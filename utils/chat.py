def get_client(api_key):
    """根据传入的 API Key 创建 Gemini SDK 客户端"""
    return genai.Client(api_key=api_key)


def chat_with_consultant(messages, api_key, file_text=None):
    """
    与 AI 简历咨询师进行对话。
    
    参数:
        messages (list): 消息历史列表
        api_key (str): Gemini API Key
        file_text (str): 可选，用户上传的文件提取的文本内容
    
    返回:
        str: AI 咨询师的回复文本
    """
    try:
        client = get_client(api_key)
        
        # 构建完整的对话内容
        contents = []
        
        for msg in messages:
            if msg["role"] == "user":
                contents.append({
                    "role": "user",
                    "parts": [{"text": msg["content"]}]
                })
            elif msg["role"] == "assistant":
                contents.append({
                    "role": "model",
                    "parts": [{"text": msg["content"]}]
                })
        
        # 在第一条用户消息前加入完整知识库 + 人设
        if contents and contents[0]["role"] == "user":
            original_text = contents[0]["parts"][0]["text"]
            
            # 如果有上传的文件内容，一并加入
            file_context = ""
            if file_text:
                file_context = f"\n\n[用户上传的文件内容]\n{file_text}\n"
            
            contents[0]["parts"][0]["text"] = (
                f"[系统指令 - 用户不可见]\n{CONSULTANT_SYSTEM_PROMPT}\n"
                f"\n{TRAINING_DATA}\n"
                f"{file_context}\n"
                f"[用户消息] {original_text}"
            )
        
        # 调用 Gemini API
        response = client.models.generate_content(
            model="gemini-2.5-flash",
            contents=contents,
        )

        return response.text or ""
    
    except Exception as e:
        return f"⚠️ 抱歉，我暂时无法回复。错误信息: {str(e)}\n\n请检查 API Key 是否正确、网络连接是否正常。"
